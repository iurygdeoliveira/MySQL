#10 - DEFINIÇÕES E CONCEITOS DE SQL 

-- A abreviatura SQL significa Structured Query Language (Linguagem
-- Estruturada de Consulta). É a linguagem padrão usada para construir e acessar
-- sistemas de bancos de dados relacionais (RDBMS ou SGBDR) de diferentes tipos,
-- e em diferentes plataformas de hardware. Apesar de ser uma linguagem padrão, há
-- diferenças entre os dialetos SQL implementados em bancos de dados SQL.

-- O SQL apresenta uma série de comandos que permitem a definição dos
-- dados, chamada de DDL (Data Definition Language), composta entre outros pelos
-- comandos Create, que é destinada à criação do banco de dados, das tabelas que o
-- compõe, além das relações existentes entre as tabelas. Como exemplo de
-- comandos da classe DDL, temos os comandos Create, Alter e Drop.

-- Os comandos da série DML (Data Manipulation Language), destinados a
-- consultas, inserções, exclusões e alterações em um ou mais registros de uma ou
-- mais tabelas de maneira simultânea. Como exemplo de comandos da classe DML,
-- temos os comandos Select, Insert, Update e Delete.

-- Uma subclasse de comandos DML, a DCL (Data Control Language), dispõe
-- de comandos de controle como Grant e Revoke.



